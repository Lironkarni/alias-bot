const DIFFICULTY_ORDER = ['easy', 'medium', 'hard'];

function getWordPool(difficulty, isPremium = false) {
  if (difficulty === 'medium') return MEDIUM_WORDS;
  if (difficulty === 'hard') return HARD_WORDS;
  return isPremium ? PREMIUM_EASY_WORDS : WORDS;
}

function nextDifficulty(current) {
  const idx = DIFFICULTY_ORDER.indexOf(current);
  return DIFFICULTY_ORDER[(idx + 1) % DIFFICULTY_ORDER.length];
}

module.exports = {
  WORDS,
  PREMIUM_EASY_WORDS,
  MEDIUM_WORDS,
  HARD_WORDS,
  FREE_WORD_LIMIT,
  DIFFICULTY_LABELS,
  DIFFICULTY_ORDER,
  getWordPool,
  nextDifficulty,
};
