function uniqueWords(words) {
  const seen = new Set();
  const result = [];

  for (const value of words) {
    const word = String(value).trim().replace(/\\s+/g, ' ');
    if (!word || seen.has(word)) continue;
    seen.add(word);
    result.push(word);
  }

  return result;
}

